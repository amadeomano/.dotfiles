// @ts-check

const { joinPathFragments } = require('@nx/devkit');
const { getNames } = require('./getNames');
const { writeContent, readContent } = require('./writeContent');
const { injectContent } = require('./injectContent');

/**
 * @param {object} options
 * @param {import('openapi-typescript').OpenAPI3} options.schema
 * @param {import('../types').RequestSyncSchema} options.options
 * @param {import('@nx/devkit').ExecutorContext} options.context
 * @param {import('./getOperations').Operations} options.operations
 */
const buildHooks = ({ operations, context }) => {
  const exports = [];
  const exportsPath = joinPathFragments('src', 'hooks', 'index.ts');

  Object.entries(operations).forEach(([operationId, operation]) => {
    const names = getNames(operationId, context);

    const importsParams = [];
    const optionsParams = [];
    const mutationParams = [];
    const axiosParam = '...axiosOptions';

    if (operation.hasPathParams) {
      importsParams.push('requestPathParams');
      optionsParams.push('pathParams');
      mutationParams.push('pathParams: requestPathParams');
    }

    if (operation.hasRequestBody) {
      importsParams.push('requestBody');
      optionsParams.push('data');
      mutationParams.push('data: requestBody');
    }

    if (operation.hasQuery) {
      importsParams.push('requestQuery');
      optionsParams.push('params');
      mutationParams.push('params: requestQuery');
    }

    if (operation.hasHeaders) {
      importsParams.push('requestHeaders');
      optionsParams.push('headers');
      mutationParams.push('headers: requestHeaders');
    }

    const fileName = joinPathFragments('src', 'hooks', `${names.hookReal}.ts`);

    const imports = importsParams.length ? `${importsParams.join(',')},` : '';
    const options = optionsParams.length ? `${optionsParams.join(',')},` : '';

    const hasRequestOptions = imports.length;

    let content = '';

    if (operation.method === 'get') {
      const hasDefaultObject = !operation.hasOptions ? '= {}' : '';

      content = `/* DO NOT DELETE - This file is automatically generated and should not be modified */

      import { useQuery } from 'react-query';
      import type { ${names.hook} } from '${names.typeModuleName}';
      import { ${names.requestReal} } from '../requests';
      import { ${names.apiReal} } from '../common';

      export const ${names.hookReal}: ${
        names.hook
      } = ({ ${imports} ...options } ${hasDefaultObject}) =>
        useQuery({
          ...options,
          queryKey: [${names.apiReal}.KEY, ${imports}],
          queryFn: (${hasRequestOptions ? '{ queryKey }' : ''}) => {
            ${hasRequestOptions ? `const [, ${options}] = queryKey;` : ''}

            return ${names.requestReal}(${
        hasRequestOptions ? `{${options}}` : ''
      });
          },
        });`;
    } else {
      content = `/* DO NOT DELETE - This file is automatically generated and should not be modified */

      import { useMutation } from 'react-query';
      import type { ${names.hook} } from '${names.typeModuleName}';
      import { ${names.requestReal} } from '../requests';
      import { ${names.apiReal} } from '../common';

      export const ${names.hookReal}: ${names.hook} = (options) =>
      useMutation({
        ...options,
        mutationKey: [${names.apiReal}.KEY],
        mutationFn: (${
          hasRequestOptions
            ? `{${importsParams.concat(axiosParam)}}`
            : `{${axiosParam}}`
        }) => {
          return ${names.requestReal}(${
        hasRequestOptions
          ? `{${mutationParams.concat(axiosParam)}}`
          : `${axiosParam}`
      });
        },
      });`;
    }

    writeContent({ content, fileName, context });

    exports.push(`export * from './${names.hookReal}';`);
  });

  const exportsContent = readContent({ fileName: exportsPath, context });

  const updatedExports = injectContent(
    'exports',
    exportsContent,
    exports.join('\n'),
  );

  writeContent({ content: updatedExports, fileName: exportsPath, context });
};

module.exports = {
  buildHooks,
};
