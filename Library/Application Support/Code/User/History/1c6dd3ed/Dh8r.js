// @ts-check

const { joinPathFragments } = require('@nx/devkit');
const { getNames } = require('./getNames');
const { writeContent, readContent } = require('./writeContent');
const { injectContent } = require('./injectContent');

/**
 * @param {object} options
 * @param {import('openapi-typescript').OpenAPI3} options.schema
 * @param {import('../types').RequestSyncSchema} options.options
 * @param {import('@nx/devkit').ExecutorContext} options.context
 * @param {import('./getOperations').Operations} options.operations
 */
const buildRequests = ({ operations, context }) => {
  const exports = [];
  const exportsPath = joinPathFragments('src', 'requests', 'index.ts');

  Object.entries(operations).forEach(([operationId, operation]) => {
    const names = getNames(operationId, context);

    const optionsProps = [];
    const importTypes = [`${names.request}`, `${names.dataResponse}`];
    let apiPath = `${names.apiReal}.API_PATH`;

    if (operation.hasRequestBody) {
      optionsProps.push('data');
    }

    if (operation.hasQuery) {
      optionsProps.push('params');
    }

    if (operation.hasPathParams) {
      importTypes.push(`${names.pathParams}`, `${names.api}`);
      optionsProps.push('pathParams');
      apiPath = `replacePathParams(${apiPath}, pathParams)`;
    }

    if (operation.hasHeaders) {
      optionsProps.push('headers');
    }

    const fileName = joinPathFragments(
      'src',
      'requests',
      `${names.requestReal}.ts`,
    );

    const axiosRequestOptions = '...axiosRequestOptions';
    const options = optionsProps.length
      ? `{ ${optionsProps.concat(axiosRequestOptions).join(',')} }`
      : `{ ${axiosRequestOptions} }`;

    const requestOptions = optionsProps
      .filter(
        (option) =>
          option === 'params' ||
          option === 'headers' ||
          option === 'data' ||
          option === axiosRequestOptions,
      )
      .concat(axiosRequestOptions)
      .join(',');

    const content = `/* DO NOT DELETE - This file is automatically generated and should not be modified */

        import request from '@personio-web/request';
        import type {
          ${importTypes.join(',')},
        } from '${names.typeModuleName}';
        import { ${names.apiReal} } from '../common';

        ${
          operation.hasPathParams
            ? `const replacePathParams = (
          url: ${names.api}['API_PATH'],
          params: ${names.pathParams},
        ) => url.replace(/:(.*?)(?![^/])/gs, (_match, key) => params[key]);`
            : ``
        }

        export const ${names.requestReal}:  ${names.request} = (${options}) =>
          request(${apiPath}, { method: ${
      names.apiReal
    }.METHOD, ${requestOptions} }).then(
            ({ data }: ${names.dataResponse}) => data,
          );`;

    writeContent({ content, fileName, context });

    exports.push(`export * from './${names.requestReal}';`);
  });

  const exportsContent = readContent({ fileName: exportsPath, context });

  const updatedExports = injectContent(
    'exports',
    exportsContent,
    exports.join('\n'),
  );

  writeContent({ content: updatedExports, fileName: exportsPath, context });
};

module.exports = {
  buildRequests,
};
